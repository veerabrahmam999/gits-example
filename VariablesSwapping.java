/*
    This class provides functionality to swap the two numbers */

import java.util.Scanner;

public class VariablesSwapping {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Enter the value of Variable1: ");
        int variable1= scanner.nextInt();
        System.out.print("Enter the value of Variable2: ");
        int variable2 = scanner.nextInt();

        int result[]=swapVaribles(variable1,variable2);
        System.out.println("A value is: "+(result[0])+" B value is: "+(result[1]));

    }
    private static int[] swapVaribles(int variable1, int variable2){
        int tempVariable=variable2;
        variable1=variable1+variable2;
        variable2=variable1-variable2;
        variable1=tempVariable;

        return new int[]{variable1,variable2};
    }

}

